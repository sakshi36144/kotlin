package operators

/*
What is Operators?
    An operator is a special symbol that perform specific task between two or more operands.

    Syntax :
        <operand> <Operator> <operand>

    Types of Operators :
        -> Arithmetic Operators : +, -, *, /, % (modulo)
        -> Assignment Operators : =, +=, -=,*=, /=, %=,
        -> Relational Operators : ==, !=, >, <, >=, <=
        -> Logical Operators : && (AND), || (OR) , ! (NOT)
        -> Unary Operators : ++, --, +, -, !
        -> Ternary Operators : ? : (kotlin not support)
        -> Bitwise Operators : & (exclusive AND), | (exclusive OR), ~ (NOT), ^ (XOR)

        */
